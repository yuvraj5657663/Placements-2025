import java.util.*;

public class FloatPoint {
    public static void main(String args[]) {
        float a = 20.5f, b = 6.4f;
        System.out.println("a= " + a);
        System.out.println("b= " + b);
        System.out.println("a+b= " + (a + b));
        System.out.println("a-b= " + (a - b));
        System.out.println("a*b= " + (a * b));
        System.out.println("a/b= " + (a / b));
        System.out.println("a%b= " + (a % b));
    }
}
