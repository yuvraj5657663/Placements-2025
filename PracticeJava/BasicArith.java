package PracticeJava;

public class BasicArith {
    public static void main(String[] args) {
        System.out.println(125 + 24);
        System.out.println(125 - 24);
        System.out.println(125 * 24);
        System.out.println(125 / 24);
        System.out.println(125 % 24);
        
    }
}
